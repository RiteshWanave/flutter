// Q1 Write C program to Increment & Decrement operations using Pointer

#include <stdio.h>

int main(){

    int a , b;
    int *ptra , *ptrb;

    scanf("%d %d" , &a , &b);

    ptra = &a;
    ptrb = &b;

    ++*ptra;
    --*ptrb;

    printf("%d and %d" , *ptra, *ptrb);

    return 0;
}



